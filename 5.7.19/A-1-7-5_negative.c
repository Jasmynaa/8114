//5.7.19　(建议) 函数中数组变量作为参数指针传递时，建议同时传递数组长度
//------维持8114的“A - 1 - 7 - 5 函数中数组变量作为参数指针传递时，建议同时传递数组长度”

void fun1(int p[])      //违背
{
	int i;
	for (i = 0; i<6; i++)
	{
		p[i] = p[i] + 1;
	}
}
void fun2(int *p)       //违背
{
	int i;
	for (i = 0; i<6; i++)
	{
		p[i] = p[i] + 1;
	}
}
int main(void)
{
	int a[6] = { 0, 1, 2, 3, 4, 5 };
	fun1(a);
	fun2(a);
	return (0);
}
